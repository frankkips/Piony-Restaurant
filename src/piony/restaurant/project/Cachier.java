    private void orderfieldMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_orderfieldMouseClicked
        // TODO add your handling code here:
        try {
            // Get The orderNo and dispaly it on the Screen
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/piony","root","");
            String query = "SELECT orderNo FROM orders ORDER BY orderNo DESC LIMIT 1;";
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery(query);
            
            if (rs.next()){
                int orderNum = rs.getInt("orderNo");
                int or = orderNum + 1;
                String oj = Integer.toString(or);
                orderfield.setText(oj);
            }else {
                orderfield.setText("1");
            }
            
        
        } catch (SQLException ex) {
            Logger.getLogger(Cachier.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Cachier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_orderfieldMouseClicked

