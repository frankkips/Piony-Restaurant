    private void receiptbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_receiptbtnActionPerformed
        // TODO add your handling code here:
        int orderNo = Integer.parseInt(orderfield.getText());
        int qty = Integer.parseInt(quantityfield.getText());
        double price = 0;
        String carbs = null;
        
        
        
        //prices
        if (radiochapati.isSelected()){
            price = 10;
            carbs = "chapati";
        }
        else if (radiochips.isSelected()){
            price = 30;
            carbs = "chips";
        }
        else if (radiomchele.isSelected()){
            price = 15;
            carbs = "mchele";
        }
        else if (radiopilau.isSelected()){
            price = 35;
            carbs = "pilau";
        }
        
        //Dispaly price 
        double carbsTotal = qty * price;
        carbpricefield.setText("" + carbsTotal);
        
        double escort = 0;
        String jina = null;
//        Escort conditions
        if (checkndengu.isSelected()){escort = 20; jina = "ndengu";}
        if (checkbeans.isSelected()){escort = 20; jina = "beans";}
        if (checkpeas.isSelected()){escort = 25; jina = "peas";}
        if (checknyama.isSelected()){escort = 70; jina = "nyama";}
        
        escortfield.setText("" + escort);
        
        
            
        
        //Total payable
        double total = carbsTotal + escort;
        amountfield.setText("" + total);
        
        //Display order details
        receipttextarea.setText("============================================\nHello your're order id is: " + orderNo + "\n " + carbs + "\t\t " + carbsTotal + "\n " + jina + "\t\t " + escort
        + "\nTotal Amount: \t\t" + total + "\n============================================");
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/piony","root","");
        
            PreparedStatement insert = con.prepareStatement("insert into orders(carbs,escort,Total)values(?,?,?)");  
            insert.setString(1, carbs);
            insert.setString(2, jina);
            insert.setDouble(3, total);
            insert.executeUpdate();
        } catch (Exception e) {
            e.printStackTrace();
        }            
        
        
    }//GEN-LAST:event_receiptbtnActionPerformed

